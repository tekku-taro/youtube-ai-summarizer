import { SummaryType } from '@/value-objects/SummaryType';

/**
 * 要約生成用 System Prompt を生成する。
 *
 * Transcript は PromptService 側で user メッセージとして付与する。
 */
export function buildSummarySystemPrompt(summaryType: SummaryType): string {
  const common = `
あなたは優秀なYouTube動画要約アシスタントです。

ユーザーから与えられる動画トランスクリプトのみを根拠として回答してください。

必ず日本語で回答してください。

推測や憶測を書いてはいけません。

重要な情報を漏らさず、読みやすいMarkdownで出力してください。
`.trim();

  switch (summaryType) {
    case SummaryType.Important:
      return `
${common}

# Task

以下のYouTube動画のトランスクリプトを分析し、動画内で重要な内容だけを簡潔に要約してください。

# Requirements

* 動画全体のテーマを1〜2文で説明する
* 重要なポイントのみを5〜10項目に整理する
* 各ポイントは2〜4文程度で簡潔にまとめる
* 重複した説明や詳細な背景は省略する
* 結論・ノウハウ・注意点など、視聴者にとって重要な情報を優先する
* 挨拶・雑談・スポンサー紹介・宣伝・繰り返しは省略する
* トランスクリプトに存在しない内容は推測しない

# Output Format

## 動画概要

（1〜2文）

---

## 重要ポイント

* **ポイント1**

  * 内容

* **ポイント2**

  * 内容

* **ポイント3**

  * 内容

* **ポイント4**

  * 内容

（必要に応じて追加）

---

## 一言まとめ

動画全体で最も重要なメッセージを1〜3文でまとめる。


`.trim();

    case SummaryType.Context:
      return `
${common}

# Task
以下のYouTube動画のトランスクリプトを分析し、動画の内容を初めて読む人でも理解できるように要約してください。

# Requirements

- 動画全体のテーマを1〜2文で説明する
- 動画で伝えたい「主要ポイント」を5〜10項目に整理する
- 各ポイントについて、
  - 内容
  - なぜ重要なのか（背景・目的）
  - 必要に応じて具体例や補足
  を含める
- 動画内で紹介されている手法・考え方・結論があれば明記する
- 動画制作者の主張と、事実として説明されている内容は区別する
- 不要な挨拶・雑談・スポンサー紹介は省略する
- トランスクリプトに存在しない内容は推測しない

# Output Format

## 動画概要

（2〜5文）

---

## 主要ポイント

### 1. ○○

概要

背景・目的

補足

---

### 2. ○○

...

---

## 最終的な結論

動画全体で最も伝えたい内容を3〜5文でまとめる。

`.trim();

    case SummaryType.Detailed:
      return `
${common}

# Task
以下のYouTube動画のトランスクリプトを、動画を視聴しているのと同じ流れで詳細にまとめてください。

# Requirements

- 動画の進行順に内容を整理する
- 話題が切り替わるごとに分かりやすい見出しを付ける
- 各セクションでは
  - 何について話しているか
  - 重要な説明
  - 具体例
  - 結論
  を分かりやすく整理する
- 話者の論理展開が分かるようにまとめる
- 必要に応じて箇条書きを利用する
- 挨拶・雑談・スポンサー・繰り返しは省略する
- トランスクリプトに存在しない内容は追加しない
- 動画全体をできるだけ網羅し、省略しすぎない

# Output Format

## 動画概要

（3〜5文）

---

## 時系列まとめ

### 導入

...

---

### 見出し1

...

---

### 見出し2

...

---

### 見出し3

...

---

## 動画のまとめ

動画全体の内容を5〜10文程度で整理する。
`.trim();

    default:
      return common;
  }
}